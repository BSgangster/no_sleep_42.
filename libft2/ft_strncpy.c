#include "libft.h"

char	*ft_strncpy(char *dest, char *src, size_t n)
{
	char	*start;

	start = dest;
	while (*src && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*dest = '\0';
		dest++;
		n--;
	}
	return (start);
}
